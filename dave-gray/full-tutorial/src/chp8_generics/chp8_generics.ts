// Using a generic in a class
// ----------------------------------------------

class StateObject<T> {
    private data: T;

    constructor(value: T) {
        this.data = value;
    }

    get state(): T {
        return this.data;
    }

    set state(value: T) {
        this.data = value;
    }
}

const store = new StateObject("John");
console.log(store.state);
store.state = "Luis";

const myState = new StateObject<(string | number | boolean)[]>([15]);
myState.state = ["Luis", true, 12];
console.log(myState.state);
