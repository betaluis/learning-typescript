// Redoing the function with an interface
// --------------------------------------------------------

interface BoolCheck<T> {
    value: T,
    is: boolean
}

const isTrue = <T>(arg: T): BoolCheck<T> => {
    if (Array.isArray(arg) && !arg.length) {
        return { value: arg, is: false };
    }
    if (isObj(arg) && !Object.keys(arg as keyof T).length) {
        return {
            value: arg,
            is: false
        }
    }
    return {
        value: arg,
        is: !!arg
    }
}

console.log([
    isTrue(false),
    isTrue(0),
    isTrue(true),
    isTrue(1),
    isTrue("Luis"),
    isTrue(""),
    isTrue(null),
    isTrue(undefined),
    isTrue({}),
    isTrue({ name: "luis", age: 27 }),
    isTrue([]),
    isTrue([1,2,3]),
    isTrue(NaN),
    isTrue(-0),
])

// Example extending interface
// ------------------------------------
interface hasID {
    id: number;
}

const processUser = <T extends hasID>(user: T): T => {
    // Process user logic
    return user;
}

console.log(processUser({ name: "Luis", id: 32 })); // Has to have ID

// Complex example with extends
const getUserProperties = <T extends hasID, K extends keyof T>(users: T[], key: K): T[K][] => {
    return users.map(user => user[key]);
}

